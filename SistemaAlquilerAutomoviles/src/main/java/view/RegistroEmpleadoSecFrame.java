    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Atributos de usuario
        String user = txtUsuario.getText().trim();
        String pass = new String(txtPass.getPassword());
        String confirm = new String(txtConPass.getPassword());
        String rolUser = (String) cmbRolUsuario.getSelectedItem();
        //Atributos para empleados
        String codigo = txtCodigo.getText().trim();
        String nombres = txtNombre.getText().trim();
        String apellidos = txtApellido.getText().trim();
        String cargo = txtCargo.getText().trim();
        String salarioTexto = txtSalario.getText().trim();
        Double salario;
        try{
            salario = Double.parseDouble(salarioTexto);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "El salario debe ser un dato numerico.");
            return;
        }
        if (user.isEmpty() || pass.isEmpty() || confirm.isEmpty() || rolUser.isEmpty() || nombres.isEmpty() || apellidos.isEmpty() || cargo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }

        if (!pass.equals(confirm)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden.");
            return;
        }
        Object[] existeAdmin = auth.admins("rolUsuario", "Administrador");
        if((int)existeAdmin[1] == 0 && !rolUser.equalsIgnoreCase("administrador")){
            JOptionPane.showMessageDialog(this, "El sistema no cuenta con ningun usuario administrador, favor crear aunque sea un usuario administrador para crear los demas usuarios.");
            return;
        }
        boolean ok = auth.register(user, pass, rolUser, codigo, nombres, apellidos, cargo, salario);
        if (ok) {
            JOptionPane.showMessageDialog(this, "El empleado y usuario se creo correctamente.");
            dispose();
            // Retornar al login si solo existe
            new MainMenuFrame().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Parece que el usuario o codigo de empleado ya existen.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

