    //Creando las columnas para la tabla
    private void setupTable() {
        String[] columnNames = {"ID", "Codigo", "Marca", "Modelo", "A単o", "Tipo", "Precio/dia", "Disponible", "Mantenimiento", "Repuestos"};
        tableModel = new DefaultTableModel(columnNames, 0){
            @Override
            public boolean isCellEditable(int fila, int columna) {
                // no permite que se modifique el id
                return columna != 0;
            }
        };
        tbVehiculos.setModel(tableModel);
    }
    //Recorriendo colecciones
    private void loadClientes(){
        Proveedor mantenimiento = null;
        Proveedor repuestos = null;
        
        tableModel.setRowCount(0);//Limpiando tabla
        List<Vehiculo> vehiculos = vehiculoDao.findAllVehiculos();
        
        for(Vehiculo v: vehiculos){
            for(Proveedor p: v.getProveedores()){
                if(p.getTipo() == TipoProveedor.MANTENIMIENTO )
                    mantenimiento = p;
                else
                    repuestos = p;
            }
            Object[] row = new Object[]{
                v.getId(),
                v.getCodigo(),
                v.getMarca(),
                v.getModelo(),
                v.getA単o(),
                v.getTipo().toString(),
                v.getPrecioDia(),
                v.isDisponible(),
                mantenimiento.getNombre(),
                repuestos.getNombre()
            };
            tableModel.addRow(row);
        }
    }

