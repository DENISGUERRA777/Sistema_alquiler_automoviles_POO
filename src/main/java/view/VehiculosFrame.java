    //Actualiza los datos de la fila seleccionada
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        //captura los datos del la fila
        int fila = tbVehiculos.getSelectedRow();
        ObjectId id = (ObjectId) tableModel.getValueAt(fila, 0);
        String codigo = (String)tableModel.getValueAt(fila, 1);
        String marca = (String)tableModel.getValueAt(fila, 2);
        String modelo = (String)tableModel.getValueAt(fila, 3);
        int a単o = (int)tableModel.getValueAt(fila, 4);
        TipoVehiculo tipo = TipoVehiculo.valueOf(tableModel.getValueAt(fila, 5).toString());
        double precio = (double)tableModel.getValueAt(fila, 6);
        boolean disponible = (boolean)tableModel.getValueAt(fila, 7);
        Proveedor provM = new Proveedor((String)tableModel.getValueAt(fila, 8), TipoProveedor.MANTENIMIENTO);
        Proveedor provR = new Proveedor((String)tableModel.getValueAt(fila, 9), TipoProveedor.REPUESTOS);
        List<Proveedor>proveedores = new ArrayList<>();
        proveedores.add(provM);
        proveedores.add(provR);
        //intenta actualizar el docuemto
        boolean  ok = vehiculoControlador.update(id, codigo, marca, modelo, a単o, tipo,precio,proveedores, disponible);
        if (ok) {
            JOptionPane.showMessageDialog(this, "Los datos del vehiculo se ha actualizado con exito.");
            dispose();
            // Retornar al login si solo existe
            new MainMenuFrame().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Ocurrio un error no se pudo actualizar datos del vehiculo.");
        }
        //vuelve a cargar el frame
        new ClientesFrame().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnActualizarActionPerformed

    //borra un registro de la db
    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        //captura los datos del la fila
        int fila = tbVehiculos.getSelectedRow();
        ObjectId id = (ObjectId) tableModel.getValueAt(fila, 0);
        //intenta borrar el registro segun id
        boolean  ok = vehiculoControlador.delete(id);
        if (ok) {
            JOptionPane.showMessageDialog(this, "El vehiculo se ha eliminado con exito.");
            dispose();
            // Retornar al login si solo existe
            new MainMenuFrame().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Ocurrio un error no se puedo eliminar el vehiculo.");
        }
        //vuelve a cargar el frame
        new ClientesFrame().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnBorrarActionPerformed

